package com.prac;

public class Stat {

	
	private int pow  = 0;
	private int dex  = 0;
	private int Int  = 0;
	private int luck = 0;
	
	
	public int getPow() {
		return pow;
	}
	public void setPow(int pow) {
		this.pow = pow;
	}
	public int getDex() {
		return dex;
	}
	public void setDex(int dex) {
		this.dex = dex;
	}
	public int getInt() {
		return Int;
	}
	public void setInt(int i) {
		Int = i;
	}
	public int getLuck() {
		return luck;
	}
	public void setLuck(int luck) {
		this.luck = luck;
	}
}
